#include <stdio.h>

void main() {
    int a = 10;
    int b = 100;
    float c = 10.5;
    float d = 100.5;

    printf("a = %i, ", a);
    printf("++a = %d. \n", ++a);

    printf("b = %i, ", b);
    printf("--b = %d. \n", --b);

    printf("c = %f, ", c);
    printf("++c = %f. \n", ++c);

    printf("d = %f, ", d);
    printf("--d = %f. \n", --d);
}
